										<h2>COMBATING STRESS IN UNIVERSITY LIFE: ALL YOU NEED TO KNOW!</h2>
										<p>Why It's Important: Often, students juggle numerous responsibilities, leading to feeling overwhelmed. Distinguishing between urgent tasks and those that can wait can prevent burnout, improve productivity, and nourish your mental health.</p>
										<p><b>Implementation:</b> List out tasks in order of importance. Break larger tasks into smaller, more achievable components. This approach makes tasks seem less daunting and provides a more precise roadmap for completion.</p>
