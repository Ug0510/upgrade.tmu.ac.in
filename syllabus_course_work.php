                        <h3 class="tmu-text-primary"><span>Review of Literature </span><span> COURSE CODE:
                                RMGT171</span></h3>
                        <hr class="horizontal-line">
                        <button class="careers-accordion">Objective <span class="icon">+</span></button>
                        <div class="panel">
                            <p>The main objective of this course is to undertake a thorough review of available
                                literature on the topic selected by the research scholar.</p>
                        </div>

                        <hr class="horizontal-line">
                        <button class="careers-accordion">Course Content <span class="icon">+</span></button>
                        <div class="panel">
                            <p>
                            <ol class="m-ol-list ps-5">
                                <li>The research scholar will review the important studies conducted at the national and
                                    international level either by individuals or organizations including government
                                    agencies and present the methodology adopted and important findings emerged from
                                    these studies. Based on this review of literature the researcher will identify the
                                    research gaps existing in the available literature and thus justifying the need for
                                    the present study.</li>
                                <li>The researcher is supposed to follow the pattern adopted in the standard national
                                    and international research journals. However, as an illustration, the pattern for
                                    reporting review of literature is as under:
                                    <ol class="m-ol-list ps-5">
                                        <li>Tiwari and Sinha (1971) gave productivity trends and factor compensation in
                                            the Indian textiles industry for the period 1946-65, comprising two
                                            sub-periods (1946-1955 and 1956-1965). The marginal productivity of capital,
                                            on average for the industry as a whole, is found to be lower in the
                                            sub-period (1946-55) and higher during (1956-65). Conversely, the estimates
                                            of the marginal productivity of labor again, on average, exhibit an actual
                                            decline from 2.9 percent to 2.5 percent.</li>
                                        <li>Menon (1971), in his review article, examines the concepts associated with
                                            the measurement of productivity at various dimensions of output and input,
                                            which are the major elements involved in the exercise of measuring
                                            productivity.</li>
                                        <li>Kumar, Anil, and Khurana (2007), in their paper, have examined trends in
                                            productivity of labor and capital in the dairy industry in India during pre
                                            and post-reform periods. The results in the study conclude that labor
                                            productivity at the national level has shown considerable improvement during
                                            the post-reform period. But variations have been observed in the growth rate
                                            of labor productivity at the state level. On the other hand, capital
                                            productivity has declined during the post-reform period at national and
                                            state levels.</li>
                                        <li>Kumar and Bala (2007), in their study on "An evaluation of the impact of
                                            economic reforms on the growth and productivity of the Indian small-scale
                                            sector," have concluded that the economic reforms process initiated in the
                                            early nineties has had a downbeat impact on the growth and productivity of
                                            the small-scale sector.</li>
                                    </ol>
                                </li>
                            </ol>
                            </p>
                        </div>
